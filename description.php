                                    <div class="panel-body">
                                        <ul>
                                            <li>Professeur : Kathryn English</li>
                                            <li>Horaire : Mardi 18h-20h</li>
                                            <li>Salle : 55</li>
                                            <li>Contenu : 
                                                <br>
                                                This course involves exploring issues inherent to communication in the globalized business world. This means using English, not as a subject for study but as a tool for international and intercultural communication.  Students should take both modules three and four.  However it is possible to follow only one module.
                                                We start with straightforward business problems/issues and present different perspectives. Students will be required to come up with and present creative solutions to hypothetical problems such as restructuring a failing airline or relocating a factory. Articles for reading will be set.
                                                Students practice effective techniques for running or participating in meetings according to American procedures.  These will be presented and simulated.  Participants then present projects which are evaluated by hypothetical investors.
                                                The different steps to negotiation (using Ury and Fischer’s work from the Harvard Negotiation Project) will be experienced via simulated projects with students working on separate teams. Teamwork is emphasized throughout the course. Strategies for effective brainstorming will also be presented.
