                                <div class="panel-heading">
                                    <h4 class="panel-title"> 
                                        <input type="checkbox" aria-label="...">
                                        <a href="#ANG464" data-toggle="collapse" data-parent="#ANG"> ANG464 : American Series </a>
                                    </h4>
                                </div>
                                <div id="ANG464" class="panel-collapse collapse">
                                    <div class="panel-body">
                                        <ul>
                                            <li>Professeur : Daivy Babel</li>
                                            <li>Horaire : Lundi 18h-20h</li>
                                            <li>Salle : 57</li>
                                            <li>Contenu : 
                                                <br>
                                                What made you think that NBC’s The Blacklist was original when you watched the very first episode? What do you mean when you say AMC's The Walking Dead is a success? What are you comparing it with/ to? How does CBS’s Person of Interest really differ in tone and atmosphere from The Wire, 24 and Homeland? Can Modern Family be compared to any other show? Perhaps, in a way, to Diff'rent Strokes (1978- 1985/1986) or The Cosby Show (1984-1992)? Have you ever seen a pattern in Prison Break/ing Bad or in Dexter? Does the British period drama television series Downton Abbey display, from a historical perspective, much originality? More than the nth superhero depiction of The Flash? – how original can a series still be after the eponymous comic books, movies, comic strips and … the American superhero TV series from 1990? 
                                                Repetition and variation are key to understanding serial TV shows; this means comparison is the first and foremost instrument to be used in analyzing them. Opposing pattern to originality will be the main focus of our studies. To gain better insights into the making of a series, but also into its cultural content as well as its impact - in particular its ideological impact - comparison can be of paramount importance. Comparing two or more cultural objects from various perspectives and on several levels can increase the number of angles from which those series can be read and understood, and can consequently lead to new and original perceptions of the series.  Beyond the comparisons per se, you will be encouraged to question the concept of comparison itself, that is to say to get perspective vis-à-vis your methodology and instruments of analysis. Can one draw conclusions when comparing two or more series from different decades? Is it useful / possible to compare two different genres at two different times? Can such an intellectual instrument of analysis and its scientific methodology help when it comes to series that you like / love / hate? 
                                                ‘On the The Big Bang Theory, how can we characterize the evolution of Bernadette Maryann Rostenkowski between her introduction in The Creepy Candy Coating Corollary and the finale of season 5, when she becomes Mrs Rostenkowski-Wolowitz? Is it parallel to that of Amy Farrah Fowler? To what extent are spoilers a much more significant problem when it comes to Game of Thrones than when it comes to Friends? Does it make more sense to compare the latter to its spin-off Joey than to Courtney Cox’s Cougar Town or to another sitcom? Does collating The Big Bang Theory (2007-) and Friends (1994-2004) show endless repetition or an evolution in sitcoms over two decades?’ are examples of questions students can ask themselves and that will lead to oral presentations, debates & discussions in class where communications skills will be sharpened thanks to regular participation. 
                                            </li>
